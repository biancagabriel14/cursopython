"""
tipos de int e float

int: numero inteiro, representa qualquer número positivo ou negativo, sem sinal é positivo

float: numero com ponto flutuante, representa qualquer numero positivo ou negativo com ponto flutuante, sem sinal é positivo

função type: mostra o tipo que o python inferiu ao valor
"""

#int

print(11)
print(-11)
print(0)

#float
print(1.1, 10.11)
print(0.0, -1.5)

#função type

print((type(132)))